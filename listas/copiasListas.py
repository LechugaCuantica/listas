# Copiar una lista
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

# Otra forma de copiar una lista
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)
print(mylist)

# Otra forma de copiar una lista
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]
print(mylist)

# Listas
thislist = ["apple", "banana", "cherry"]
print(thislist)

# Permite duplicados
thislist = ["apple", "banana", "cherry", "apple", "cherry"]
print(thislist)

# Imprimer cuantos elementos hay en una lista
thislist = ["apple", "banana", "cherry"]
print(len(thislist))

